package com.example.hometaskandroid_03_08.models;

public class SecondFrag extends JustClass{
    private final String textOne;
    private final String textTwo;

    public SecondFrag(String textOne, String textTwo) {
        this.textOne = textOne;
        this.textTwo = textTwo;
    }

    public String getTextOne() {
        return textOne;
    }

    public String getTextTwo() {
        return textTwo;
    }
}
